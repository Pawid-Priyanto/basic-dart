main() {
  print('string pakai petik satu');
  print("String pake petik dua");

  // String can be combined with the + operator.
  print('Cat' + 'Dog');

  // Triple quotes define a multi-line string
  print('''triple quoted string
  are for multiple lines''');

  //  Dart supports string interpolation.
  const pi = 3.14;
  print('pi is $pi');
  print('hasil dari ${2 * pi}');

}

